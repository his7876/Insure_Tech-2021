function plus(p1, p2){
    return p1+p2;
}

// Arrow function 
const plus2 = (p1, p2 ) => {
    return p1 + p2; 
};

const minus = (p1, p2 ) => {
    return p1 - p2; 
};

const multiple = (p1, p2 ) => {
    return p1 * p2; 
};

const division = (p1, p2 ) => {
    return p1 / p2; 
};



